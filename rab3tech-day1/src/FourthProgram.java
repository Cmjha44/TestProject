

public class FourthProgram {

	public static void main(String[] args) {
		//define variable
				//for storing any data two things are required(type of data and name of variable)
				//integer type of data
				int age=20;
				System.out.println("Age is="+age);
				//here age is variable name
				
				//float type of data
				float free=4.5f;  //u may use small f  or capital F (only f/F)
				//here free is variable name
				System.out.println("Float value==="+free);
				
				
				//string type of data
				String name="Rojina"; 
				//for storing String type of that we use double quote(" ")
				System.out.println("name of candidate is=="+name);
				
				//character type of data
				char ch='m';
					//for storing character type of data we use single quote ('  ')
				System.out.println("Character value is="+ch);
					

	}

}
