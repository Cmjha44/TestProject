package iteration.statement;

public class ForLoopDemoa {

	public static void main(String[] args) {
		// printing table from 3 to 12
				for(int i = 3; i<=12;i++) {
					for(int j=1;j<=12;j++) {
						System.out.print(i+" * "+j+" = ");
						System.out.println(i*j);
					}
					System.out.println("*********************");
					
				}
				
	}
	
}
				