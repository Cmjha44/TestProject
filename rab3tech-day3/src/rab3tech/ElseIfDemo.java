package rab3tech;

public class ElseIfDemo {

	public static void main(String[] args) {
		int pmtime =12;
		if(pmtime<12) {
			System.out.println("Good Morning.");
		}
		else if(pmtime==12) {
			System.out.println("Its Mid-day right now.");
		}
		else {
			System.out.println("Good Afternoon.");
		}

		
	}

}
