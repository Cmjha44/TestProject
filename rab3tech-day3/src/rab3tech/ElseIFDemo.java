package rab3tech;

public class ElseIFDemo {

	public static void main(String[] args) {
		int pmtime=13;
		if (pmtime<12) {
			System.out.println("Good morning");
		}
		else if (pmtime==12) {
			
			System.out.println("It's Mid day right now. ");
		}
		else {
			System.out.println("Good afternoon.");
		}
	}

}
