package selectin.statement;

public class SwitchCaseDemo2 {

	public static void main(String[] args) {
		//define variable
		char ch='d';
		
		
		
		switch(ch) {
		
		case 'a':
				System.out.println("This is case A");
				break;
				
		case 'd':
			System.out.println("This is case D");
			break;
		case 'f':
			System.out.println("This is case F");
			break;
		case 'p':
			System.out.println("This is case P");
			break;
			
		case 'm':
			System.out.println("This is case M");
			break;
			
		
		}
		

	}

}
